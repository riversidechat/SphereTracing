function GetKeyState(key) {
  return Keys_state[((key - 8) * 2) + 1];
}
var Keyboard = {
  event: function(e) {
    var key_state = (e.type === "keydown")? true : false;
    Keys_state[((e.keyCode - 8)*2)+1] = key_state;
  },
}
document.addEventListener("keydown", Keyboard.event);
document.addEventListener("keyup", Keyboard.event);


var Mouse = {
  x: 0,
  y: 0,
  pressed: false,
  held: false,
  ticksHeld: 0,
  Move: function(e)
  {
    Mouse.x = e.x;
    Mouse.y = e.y;
  },
  Event: function(e)
  {
    var state = (e.type == "mousedown")? true : false;
    Mouse.pressed = state;
    Mouse.held = state;
    Mouse.ticksHeld = 0;
  },
  Update: function()
  {
    Mouse.pressed = false;
    if(Mouse.held) Mouse.ticksHeld ++;
  }
}
document.addEventListener("mousemove", Mouse.Move);
document.addEventListener("mousedown", Mouse.Event);
document.addEventListener("mouseup", Mouse.Event);